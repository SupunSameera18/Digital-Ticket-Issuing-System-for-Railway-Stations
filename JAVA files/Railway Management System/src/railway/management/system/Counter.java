            ticket.jLabel18.setText(train);
            ticket.jLabel19.setText(clss);
            ticket.jLabel20.setText(arrTime);
            ticket.jLabel21.setText(strDistance);
            ticket.jLabel32.setText(Integer.toString(seatID));
            ticket.jLabel30.setText(depTime);
