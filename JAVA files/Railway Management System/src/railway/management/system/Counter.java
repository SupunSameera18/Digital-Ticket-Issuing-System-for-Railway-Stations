    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed

        // TODO add your handling code here:
        int stationID = 0;
        int trainID = 0;
        int tripID = 0;
        int seatID = 0;
        String status = "";
        String destination = jComboBox1.getSelectedItem().toString();
        String train = jComboBox2.getSelectedItem().toString();
        String clss = jComboBox3.getSelectedItem().toString();
        Date date = jDateChooser1.getDate();
        SimpleDateFormat format = new SimpleDateFormat("dd-MMM-yyyy");
        String strDate = format.format(date);

        bk = new Bookings();

        DefaultTableModel model = (DefaultTableModel) bk.jTable1.getModel();

        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/railway management system", "root", "");

            //get data from seats table and making booking table
            pst = con.prepareStatement("select * from seats where Class =  ?");
            pst.setString(1, clss);
            rs = pst.executeQuery();
            if (!rs.next()) {
                JOptionPane.showMessageDialog(null, "Error1");
            } else {
                do {
                    seatID = rs.getInt("Seat_ID");
                    status = rs.getString("Status");
                    String row[] = {Integer.toString(seatID), clss, status};
                    model.addRow(row);
                } while (rs.next());
                bk.main();
            }

            //get data from stations table
            pst = con.prepareStatement("select * from stations where Station_Name =  ?");
            pst.setString(1, destination);
            rs = pst.executeQuery();
            if (!rs.next()) {
                JOptionPane.showMessageDialog(null, "Error2");
            } else {
                stationID = rs.getInt("Station_ID");
            }

            //get data from trains table
            pst = con.prepareStatement("select * from trains where Train_Name =  ?");
            pst.setString(1, train);
            rs = pst.executeQuery();
            if (!rs.next()) {
                JOptionPane.showMessageDialog(null, "Error3");
            } else {
                trainID = rs.getInt("Train_ID");
            }

            //Recieve data from trip table 
            if (stationID < 3) {
                pst = con.prepareStatement("select * from trips where Train_ID = ? && Start_Station_ID = 6");
                pst.setInt(1, trainID);
                rs = pst.executeQuery();
                if (!rs.next()) {
                    JOptionPane.showMessageDialog(null, "Error4");
                } else {
                    tripID = rs.getInt("Trip_ID");
                }
            } else {
                pst = con.prepareStatement("select * from trips where Train_ID = ? && Start_Station_ID = 1");
                pst.setInt(1, trainID);
                rs = pst.executeQuery();
                if (!rs.next()) {
                    JOptionPane.showMessageDialog(null, "Error5");
                } else {
                    tripID = rs.getInt("Trip_ID");
                }
            }

            //get data from tickets table
            pst = con.prepareStatement("select * from tickets where Trip_ID = ? && Date = ?");
            pst.setInt(1, tripID);
            pst.setString(2, strDate);
            rs = pst.executeQuery();
            if (rs.next()) {
                //if rs have any bookings
                do {
                    seatID = rs.getInt("Seat_ID");
                    for (int i = 0; i < model.getRowCount(); i++) {
                        int temp = Integer.parseInt(model.getValueAt(i, 0).toString());
                        if (temp == seatID) {
                            model.setValueAt("Booked", i, 2);
                        }
                    }
                } while (rs.next());
            }

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Counter.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Counter.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

