            if (rs.next()) {
                ticketNumber = rs.getInt(1);
            }

            //Send data to transactions table
            pst = con.prepareStatement("insert into transactions(Ticket_Number,Customer_ID,Price,Date_Issued) values(?,?,?,?)");
            pst.setInt(1, ticketNumber);
            pst.setInt(2, customerID);

